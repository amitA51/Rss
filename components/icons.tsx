// A simple hashing function to convert a string to a number.
const stringToHash = (str: string): number => {
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
        hash = hash & hash; // Convert to 32bit integer
    }
    return hash;
};

// Generates a vibrant HSL color from a string.
export const getTagColor = (tagName: string): { backgroundColor: string; textColor: string } => {
    const hue = Math.abs(stringToHash(tagName)) % 360;
    const saturation = 60;
    const lightness = 30; // Darker background for better contrast
    const textLightness = 85; // Light text color

    return {
        backgroundColor: `hsl(${hue}, ${saturation}%, ${lightness}%)`,
        textColor: `hsl(${hue}, ${saturation}%, ${textLightness}%)`,
    };
};

export const getFileIcon = (mimeType: string) => {
    if (mimeType.startsWith('image/')) return <ImageIcon className="h-5 w-5 text-purple-400" />;
    if (mimeType.startsWith('audio/')) return <AudioFileIcon className="h-5 w-5 text-teal-400" />;
    if (mimeType === 'application/pdf') return <PdfIcon className="h-5 w-5 text-red-400" />;
    if (mimeType.includes('document') || mimeType.includes('msword')) return <DocIcon className="h-5 w-5 text-blue-400" />;
    return <FileIcon className="h-5 w-5 text-gray-400" />;
};

